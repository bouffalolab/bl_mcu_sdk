void oled_draw_point(uint8_t x, uint8_t y, uint8_t mode)
{
    uint8_t i, j, temp;

    if (x > 127 || y > 63)
        return;

    i = 7 - y / 8;
    j = y % 8;
    temp = 0x01 << (7 - j);
    if (mode == 0)
        OLED_GRAM[x][i] &= ~temp;
    else
        OLED_GRAM[x][i] |= temp;
}

void oled_draw_char(uint8_t x, uint8_t y, uint8_t chr, uint8_t size, uint8_t mode)
{
    uint8_t temp, t, t1;
    uint8_t y0 = y;
    uint8_t csize = (size / 8 + ((size % 8) ? 1 : 0)) * (size / 2);
    chr = chr - ' ';

    for (t = 0; t < csize; t++) {
        switch (size) {
            case 12:
                temp = asc2_1206[chr][t];
                break;
            case 16:
                temp = asc2_1608[chr][t];
                break;
            case 24:
                temp = asc2_2412[chr][t];
                break;
            default:
                return;
        }

        for (t1 = 0; t1 < 8; t1++) {
            if (temp & 0x80)
                oled_draw_point(x, y, mode);
            else
                oled_draw_point(x, y, !mode);

            temp <<= 1;
            y++;

            if ((y - y0) == size) {
                y = y0;
                x++;
                break;
            }
        }
    }
}

void oled_draw_string(uint8_t x, uint8_t y, const uint8_t *p, uint8_t size, uint8_t mode)
{
    while ((*p <= '~') && (*p >= ' ')) {
        if (x > (128 - (size / 2))) {
            x = 0;
            y = y + size;
        }
        if (y > (64 - size)) {
            x = y = 0;
            oled_clear();
        }

        oled_draw_char(x, y, *p, size, mode);

        x = x + size / 2;
        p++;
    }
}

void oled_draw_chinese(uint8_t x, uint8_t y, uint8_t no, uint8_t mode)
{
    uint8_t temp1;
    uint8_t temp2;
    uint8_t t1, t2;
    uint8_t x0 = x, y0 = y;

    for (t1 = 0; t1 < 16; t1++) {
        temp1 = Hzk[2 * no][t1];
        for (t2 = 0; t2 < 8; t2++) {
            if (temp1 & 0x01)
                oled_draw_point(x, y, mode);
            else
                oled_draw_point(x, y, !mode);
            temp1 >>= 1;
            y++;
        }
        y = y0;
        x++;
    }

    x = x0;
    y = y0 + 8;
    y0 = y;

    for (t1 = 0; t1 < 16; t1++) {
        temp2 = Hzk[2 * no + 1][t1];
        for (t2 = 0; t2 < 8; t2++) {
            if (temp2 & 0x01)
                oled_draw_point(x, y, mode);
            else
                oled_draw_point(x, y, !mode);
            temp2 >>= 1;
            y++;
        }
        y = y0;
        x++;
    }
}

void oled_draw_bmp(uint8_t x, uint8_t y, const unsigned char *image, uint8_t index, uint8_t mode)
{
    uint8_t temp, t1;
    uint16_t i;
    uint8_t y0 = y;

    for (i = 0; i < 1024; i++) {
        temp = image[1024 * index + i];
        for (t1 = 0; t1 < 8; t1++) {
            if (temp & 0x80)
                oled_draw_point(x, y, mode);
            else
                oled_draw_point(x, y, !mode);
            temp <<= 1;
            y++;
            if ((y - y0) == 64) {
                y = y0;
                x++;
                break;
            }
        }
    }
}

void oled_draw_alpha(uint8_t x, uint8_t y, const unsigned char *image, uint8_t index, uint8_t mode)
{
    uint8_t temp, t1;
    uint16_t i;
    uint8_t y0 = y;

    for (i = 0; i < 1024; i++) {
        temp = image[1024 * index + i];
        for (t1 = 0; t1 < 8; t1++) {
            if (temp & 0x80)
                oled_draw_point(x, y, mode);
            temp <<= 1;
            y++;
            if ((y - y0) == 64) {
                y = y0;
                x++;
                break;
            }
        }
    }
}

